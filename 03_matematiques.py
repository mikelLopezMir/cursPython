# Podem realitzar operacions amb nombres
import math

print(3+4)
print(56-23)
print(4*2)

# La divisió és un cas una mica especial. Analitza què està passant
print(18/3)
print(18/4)
print(18//4)
print(18%4)

# També puc fer el mateix amb variables
primer_valor = 23
segon_valor = 12
print(primer_valor + segon_valor)
print(primer_valor - segon_valor)
print(primer_valor * segon_valor)
print(primer_valor / segon_valor)
print(round(primer_valor / segon_valor, 2))

# Altres operacions matemàtiques
print(2**3)
print(81**.5)
print(math.sqrt(81))
print(math.factorial(4))